def tooltip_for_menu_label(label: str) -> str:
    """메뉴 항목 라벨에 대한 등록 툴팁. 없으면 빈 문자열."""
    key = menu_label_key(label)
    return MENU_LABEL_TOOLTIPS.get(key, "")


def menubar_top_tooltip(label: str) -> str:
    k = (label or "").strip().replace(" ▼", "").replace("▼", "").strip()
    return MENUBAR_TOP_TOOLTIPS.get(k, "")
