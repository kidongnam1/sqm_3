from __future__ import annotations

import re
from typing import Dict

TOOLTIP_MAX = 60

_BADGE_SUFFIX = re.compile(r"\s+[🔴🟡]\s*\[\d+\]\s*$")


def clip_tooltip(text: str) -> str:
    t = (text or "").strip()
    if len(t) <= TOOLTIP_MAX:
        return t
    return t[: TOOLTIP_MAX - 3].rstrip() + "..."


def menu_label_key(raw: str) -> str:
    s = (raw or "").strip()
    s = _BADGE_SUFFIX.sub("", s)
    return s.strip()


def _pairs(items: list[tuple[str, str]]) -> Dict[str, str]:
    out: Dict[str, str] = {}
    for label, tip in items:
        out[label.strip()] = clip_tooltip(tip)
    return out


